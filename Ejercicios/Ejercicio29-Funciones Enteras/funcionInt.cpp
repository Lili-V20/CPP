#include <iostream>

using namespace std;

int sumar(int a, int b){
    return a + b;
}

int main(int argc, char const *argv[])
{
    system ("cls");
    cout << sumar(5, 7);
    cout << endl;
    cout << sumar(10, 7);
    cout << endl;
    cout << sumar(5, 18);
    cout << endl;
    cout << sumar(1, 7);
    cout << endl;
    return 0;
}
